def month_from_ru_to_eng(month):
    if month == 'января':
        return 'Январь'
    if month == 'декабря':
        return 'Декабрь'
    if month == 'февраля':
        return 'Февраль'
    if month == 'марта':
        return 'Март'
    if month == 'апреля':
        return 'Апрель'
    if month == 'мая':
        return 'Май'
    if month == 'июня':
        return 'Июнь'
    if month == 'июля':
        return 'Июль'
    if month == 'августа':
        return 'Август'
    if month == 'сентября':
        return 'Сентябрь'
    if month == 'октября':
        return 'Октябрь'
    if month == 'ноября':
        return 'Ноябрь'
    if month == 'декабря':
        return 'Декабрь'